    private void listarFavoritos() {
    try {
        java.sql.Connection con = DAO.ConexaoDB.getConexao();
        String sql = "SELECT nome, progresso FROM jogos WHERE favorito = 1";
        PreparedStatement stmt = con.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();
        DefaultTableModel modelo = (DefaultTableModel) tabelaFavJogos.getModel();
        modelo.setRowCount(0);

        while (rs.next()) {
            String JOGO = rs.getString("nome");
            String PORCENTAGEM = rs.getString("progresso");

            modelo.addRow(new Object[]{JOGO, PORCENTAGEM + "%"});
        }

        rs.close();
        stmt.close();
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar favoritos: " + e.getMessage());
    }
}
